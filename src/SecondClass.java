public class SecondClass {
    private int one;
    private int two;
    private String command="Тест";

    public SecondClass(int one, int two) {
        this.one = one;
        this.two = two;
    }


    public int getOne() {
        return one;
    }

    public void setOne(int one) {
        this.one = one;
    }

    public int getTwo() {
        return two;
    }

    public void setTwo(int two) {
        this.two = two;
    }

    public String getCommand() {
        return command;
    }

    public void setCommand(String command) {
        this.command = command;
    }
}
